    private void hitungDiskon() {
    try {
        double harga = Double.parseDouble(txtHarga.getText());
        String diskonStr = cmbDiskon.getSelectedItem().toString().replace("%", "");
        int diskonPersen = Integer.parseInt(diskonStr);

        // Tambahan dari slider
        int diskonSlider = sldDiskon.getValue();

        // Gabungkan dua sumber diskon (ambil yang terbesar)
        int totalDiskon = Math.max(diskonPersen, diskonSlider);

        // Cek kupon tambahan
        String kupon = txtVoucher.getText().trim();
        if (kupon.equalsIgnoreCase("HEMAT10")) {
            totalDiskon += 10;
        } else if (kupon.equalsIgnoreCase("SUPER25")) {
            totalDiskon += 25;
        }

        // Batasi diskon maksimal 100%
        if (totalDiskon > 100) totalDiskon = 100;

        // Hitung penghematan dan harga akhir
        double hemat = harga * totalDiskon / 100;
        double hargaAkhir = harga - hemat;

        // Tampilkan hasil
        txtHemat.setText(String.format("Rp %.2f", hemat));
        txtHasil.setText(String.format("Rp %.2f", hargaAkhir));

        // Tambahkan ke riwayat
        txtRiwayat.append(
            "Harga: Rp " + harga +
            " | Diskon: " + totalDiskon + "%" +
            " | Hemat: Rp " + String.format("%.2f", hemat) +
            " | Harga Akhir: Rp " + String.format("%.2f", hargaAkhir) + "\n"
        );

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan angka yang valid untuk harga!", "Error", JOptionPane.ERROR_MESSAGE);
    }
}


